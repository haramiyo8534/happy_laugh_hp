<script>
$( function() {
	$(window).scroll(function () {
		$('header').toggleClass('fixed', $(this).scrollTop() > $(window).height() / 2);

		if ($(window).scrollTop() >= $(window).height() / 2) {
			$('.fix-box').addClass('show');
		} else {
			$('.fix-box').removeClass('show');
		}
	});

	$('#sp-menu-btn').on('click', function () {
		$('.header-menu').toggleClass('show');
		$(this).toggleClass('active');
		if ($(this).hasClass('active')) {
			$('body').css('overflow', 'hidden');
		} else {
			$('body').css('overflow', 'auto');
		}
	});

	$('a[href*="#"]').on('click', function (e) {
		e.preventDefault();
		if ($('.header-menu').hasClass('show')) {
			$('.header-menu').removeClass('show');
			$('body').css('overflow', 'auto');
			$('#sp-menu-btn').removeClass('active');
		}
		if($(window).width() > 768) {
			$top_offset = $('header').height();
		} else {
			$top_offset = 0;
		}
		$('html, body').animate(
			{
				scrollTop: $($(this).attr('href')).offset().top - $top_offset,
			},
			500,
		);
	});
} );
</script>

